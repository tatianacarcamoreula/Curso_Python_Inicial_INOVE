# Función para calcular la resta entre dos números
def restar(numero_1, numero_2):
    resultado = numero_1 - numero_2
    print(f"El resultado de restar {numero_1} y {numero_2} es {resultado}")


# Invocar la función restart
numero_1 = 7
numero_2 = 5
restar(numero_1, numero_2)
