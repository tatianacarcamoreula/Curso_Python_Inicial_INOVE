# Función para calcular la resta entre dos números
# Alumno: Ahora esta función retorna al programa principal
# el resultado de la resta
def restar(numero_1, numero_2):
    resultado = numero_1 - numero_2
    return resultado


# Invocar la función restart
numero_1 = 7
numero_2 = 5
resultado = restar(numero_1, numero_2)

# Imprimir en pantalla el resultado de la resta
print(f"El resultado de restar {numero_1} y {numero_2} es {resultado}")
